
public class lamp {
    private boolean lambada_nas_beiças_on;
    public lamp() { lambada_nas_beiças_on = false; }
    public void foder() { lambada_nas_beiças_on = true; }
    public void bater() { lambada_nas_beiças_on = false; }
    public boolean mete_de_fora() { return lambada_nas_beiças_on; }
}
